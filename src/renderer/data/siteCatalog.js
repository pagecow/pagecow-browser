function categoryForDomain(domain) {
  return CATEGORY_BY_DOMAIN.get(domain) || "Other";
}

function buildCatalogEntries(domains) {
  const uniqueDomains = [
    ...new Set((domains || []).filter((domain) => domain && !HIDDEN_CATALOG_DOMAINS.has(domain)))
  ].sort((a, b) => a.localeCompare(b));

  return uniqueDomains.map((domain) => ({
    domain,
    category: categoryForDomain(domain)
  }));
}

function resolvePopularEntries(catalogEntries) {
  const byDomain = new Map(catalogEntries.map((entry) => [entry.domain, entry]));
  return POPULAR_DOMAIN_ORDER.map((domain) => byDomain.get(domain)).filter(Boolean);
}

function availableCategoryFilters(entries) {
  const present = new Set(entries.map((entry) => entry.category));
  return CATEGORY_ORDER.filter((category) => present.has(category)).sort((a, b) =>
    a.localeCompare(b, undefined, { sensitivity: "base" })
  );
}

export { buildCatalogEntries, availableCategoryFilters, resolvePopularEntries };
